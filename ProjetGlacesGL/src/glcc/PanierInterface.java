    private void retirerElementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirerElementActionPerformed
    int selectedIndex = jList1.getSelectedIndex(); // Récupère l'index de l'élément sélectionné
    if (selectedIndex != -1) { // Vérifie si un élément est sélectionné
        listModel.remove(selectedIndex); // Supprime l'élément du modèle
        double total = mettreAJourTotal(); // Recalcule le total après la suppression
        glaceInterface.mettreAJourPrixPrincipal(total); // Synchronise avec GlaceInterface
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un élément à retirer.", "Aucun élément sélectionné", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_retirerElementActionPerformed

