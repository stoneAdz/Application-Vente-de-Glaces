 public void ajouterGlaceAuPanier(Glace glace) {
    listModel.addElement(glace); // Ajouter l'objet Glace au modèle de liste
    mettreAJourTotal(); // Mettre à jour le total
}
public double mettreAJourTotal() {
    double total = 0.0;

    // Parcourir chaque glace dans le modèle
    for (int i = 0; i < listModel.getSize(); i++) {
        Glace glace = listModel.getElementAt(i);
        total += glace.getPrix(); // Ajouter le prix de chaque glace
    }

    // Mettre à jour le label affichant le total
    prix.setText(total + " €");
    return total;
}

public void rechargerPanier() {
    // Met à jour la liste ou les données affichées dans le JList
    jList1.updateUI(); // Recharge les données visibles
}

private void viderPanier() {
    listModel.clear(); // Vide tous les éléments du modèle
    double total = mettreAJourTotal(); // Réinitialise le total dans PanierInterface
    glaceInterface.resetTotalPrix(); // Réinitialise totalPrix dans GlaceInterface
    glaceInterface.mettreAJourPrixPrincipal(total); // Met à jour l'affichage dans GlaceInterface
    JOptionPane.showMessageDialog(this, "Le panier a été vidé.", "Panier vidé", JOptionPane.INFORMATION_MESSAGE);
}


private static class GlaceListCellRenderer extends DefaultListCellRenderer {
    @Override
    public java.awt.Component getListCellRendererComponent(
            JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
        // Appeler la méthode parente pour configurer les propriétés par défaut
        JLabel label = (JLabel) super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

        // Vérifier que l'objet est bien une instance de Glace
        if (value instanceof Glace) {
            Glace glace = (Glace) value;
            label.setText(glace.getDesc()); // Afficher la description
        }

        return label;
    }
}

@Override
public void dispose() {
    super.dispose();
    if (glaceInterface != null) {
        double totalPrix = mettreAJourTotal(); // Recalculez le prix total
        glaceInterface.mettreAJourPrixPrincipal(totalPrix); // Met à jour dans GlaceInterface
    }
}

public GlaceInterface getGlaceInterface() {
    return glaceInterface;
}








